# -----------------------------
# нормализация текста
# -----------------------------
def normalize(text):
    text = text.lower()
    text = text.replace("ё", "е")

    # убираем ссылки
    text = re.sub(r"http\S+", " ", text)
    text = re.sub(r"t\.me/\S+", " ", text)

    # убираем всё кроме букв, цифр и пробелов
    text = re.sub(r"[^a-zа-яіїєґ0-9\s]", " ", text)

    # схлопываем 3 и более одинаковых символа подряд до одного
    text = re.sub(r"(.)\1{2,}", r"\1", text)

    # убираем лишние пробелы
    text = re.sub(r"\s+", " ", text).strip()

    return text


# -----------------------------
# обработчик сообщений
# -----------------------------
@client.on(events.NewMessage)
async def handler(event):

    if event.out:
        return

    text_raw = event.raw_text
    if not text_raw:
        return

    text = normalize(text_raw)

    if not text:
        return

    # проверка ban слов
    for word in ban_words:
        if word in text:
            return

    # поиск ключевых слов
    found_word = None
    for word in keywords:
        if word in text:
            found_word = word
            break

    if not found_word:
        return

    sender = await event.get_sender()
    chat = await event.get_chat()

    username = sender.username if sender and sender.username else "без_username"
    chat_name = getattr(chat, "title", "ЛС")

    msg = f"""
НАЙДЕНО СОВПАДЕНИЕ

Слово: {found_word}
Группа: {chat_name}
Автор: @{username}
Время: {event.date}

Сообщение:
{text_raw}
"""

    await client.send_message(alert_chat, msg)


client.start()
